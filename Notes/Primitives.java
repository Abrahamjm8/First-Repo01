package Notes;
public class Primitives {
    /*
     * Java represents the most basic data struvtures called primitives:
     *  these data types are the simplidest ones you use in java. 
     */

     //these primitives rpes represent whole numners 
     byte b = 0; //8 bits
     short s = 0; //16 bits
     int i = 0; //32 bits
     long l = 0; //64 bits

     //these primitives types represent numbers with decimals 
     float f = 0f; // 32 bits, if you dont specify f it will default to a double 
     double d = 0; // 64 bits 

     //this primitves represents the unicode character
     char c = 'c'; //16 bits

     //this primitive represents logical true or false
     boolean bb = true; // 1 bit 

}
